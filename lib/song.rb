class Song
  attr_accessor :name, :artist, :genre

  @@count = 0
  @@artists = []
  @@genres = []


  def initialize(name, artist, genre)
    @name = name
    @artist = artist
    @genre = genre
    @@count += 1
    @@genres << genre
    @@artists << artist
  end

  def self.count
    @@count
  end

  def self.genres
    @@genres.uniq
  end

  def self.artists
    @@artists.uniq
  end

  def self.genre_count
    # Hash[*@@genres.group_by{|genre| genre }.flat_map{ |genre_key, genre_count| [genre_key, genre_count.size]}]
    genre_count = {}
    @@genres.each do |genre|
      if genre_count[genre]
        genre_count[genre] += 1
      else
        genre_count[genre] = 1
      end
    end
    genre_count
  end

  def self.artist_count
    Hash[*@@artists.group_by{|artist| artist }.flat_map{ |artist_key, artist_count| [artist_key, artist_count.size]}]
  end
end
